# String interpolation lets you embed expressions in a string

name = "bob"

# Instead of using the + operater, you can use the #{} syntax
puts "Hello #{name}"


# Another example, using multiple strings
saturday = "Saturday"
friday = "Friday"

puts "My favorite days of the week are #{friday} and #{saturday}";


# String interploation can also convert different data types to strings
john = "John"
age = 18

puts "#{john} is #{age}"


